class Historia {
  final int id;
  final String nombre;
  final String url;
  final String descripcion;
  final bool favorito;
  final String usuario;
  final int puntaje;
  final String comentario;

  Historia(this.id, this.nombre, this.url, this.descripcion, this.favorito, this.usuario,
      this.puntaje, this.comentario);
}